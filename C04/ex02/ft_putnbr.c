#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
		return ((void)write(1, "-2147483648", 11));
	nb < 0 ? write(1, "-", 1) : 0;
	nb < 0 ? nb *= -1 : 0;
	nb >= 10 ? ft_putnbr(nb / 10) : 0;
	nb = nb % 10 + 48;
	write(1, &nb, 1);
}
