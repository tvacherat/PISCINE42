char	*ft_strcapitalize(char *str)
{
	unsigned int i;

	i = 1;
	if (str[0] > 96 && str[0] < 122)
		str[0] -= 32;
	while (str[i])
	{
		if (str[i] > 96 && str[i] < 123 && (str[i - 1] < 48
			|| (str[i - 1] < 65 && str[i - 1] > 57) || (str[i - 1] > 90
				&& str[i - 1] < 97) || str[i - 1] > 122))
			str[i] -= 32;
		else if (str[i] > 64 && str[i] < 91 && ((str[i - 1] > 47
			&& str[i - 1] < 58) || (str[i - 1] > 64 && str[i - 1] < 91)
				|| (str[i - 1] > 96 && str[i - 1] < 123)))
			str[i] += 32;
		i++;
	}
	return (str);
}
